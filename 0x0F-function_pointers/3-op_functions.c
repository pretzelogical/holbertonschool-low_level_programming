#include "3-calc.h"

/**
 * op_add- returns the sum of a & b
 * @a: first number to add
 * @b: second number to add
 *
 * Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- returns the difference
 * of a & b
 * @a: first number to subtract
 * @b: second number to subtract
 *
 * Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- returns the product
 * of a & b
 * @a: first number to multiply
 * @b: second number to multiply
 *
 * Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- returns the result
 * of division of a & b
 * @a: first number to divide
 * @b: second number to divide
 *
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- returns the remainder
 * of division of a & b
 * @a: first number to modulo
 * @b: second number to modulo
 *
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
